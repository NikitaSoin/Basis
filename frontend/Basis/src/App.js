const roundFV = (v) => Math.round(v / 10) * 10;

const buildFairValue = (price, fallback = 100) => {
  const p = price ?? fallback;
  return {
    dcf: {
      bear: roundFV(p * 0.8),
      base: roundFV(p * 1.02),
      bull: roundFV(p * 1.22),
    },
    pe: {
      bear: roundFV(p * 0.72),
      base: roundFV(p * 0.92),
      bull: roundFV(p * 1.08),
    },
    yield: {
      bear: roundFV(p * 0.62),
      base: roundFV(p * 0.82),
      bull: roundFV(p * 0.96),
    },
  };
};

