export default function App() {
  const apiUrl = process.env.REACT_APP_API_URL || "http://localhost:8000";

  const [activeTab, setActiveTab] = useState("landing");
  const [selectedCompany, setSelectedCompany] = useState(null);
  const [theme, setTheme] = useState(() => {
    const stored = localStorage.getItem("basis_theme");
    if (stored === "dark" || stored === "light") return stored;
    // No explicit choice → default LIGHT (per design constitution; mirrors anti-FOUC script).
    return "light";
  });

  const [user, setUser] = useState(() => {
    try { return JSON.parse(localStorage.getItem("basis_user")); } catch { return null; }
  });
  const [token, setToken] = useState(() => localStorage.getItem("basis_token") || null);
  const [showAuthModal, setShowAuthModal] = useState(false);

  useEffect(() => {
    const el = document.documentElement;
    el.classList.toggle("dark", theme === "dark");
    // Keep data-theme in sync for backward-compatible selectors.
    el.setAttribute("data-theme", theme);
    localStorage.setItem("basis_theme", theme);
  }, [theme]);

  useEffect(() => {
    if (!token) return;
    fetch(`${apiUrl}/api/auth/me`, { headers: { Authorization: `Bearer ${token}` } })
      .then(r => r.ok ? r.json() : null)
      .then(u => {
        if (u) { setUser(u); localStorage.setItem("basis_user", JSON.stringify(u)); }
        else handleLogout();
      })
      .catch(() => {});
  }, []);

  const handleLogin = (newUser, newToken) => {
    setUser(newUser);
    setToken(newToken);
    setShowAuthModal(false);
  };

  const handleLogout = () => {
    localStorage.removeItem("basis_token");
    localStorage.removeItem("basis_user");
    setUser(null);
    setToken(null);
    setActiveTab("landing");
  };

  const navigate = (tab) => {
    setActiveTab(tab);
    setSelectedCompany(null);
  };

  const renderView = () => {
    if (selectedCompany) {
      return <CompanyCard company={selectedCompany} onBack={() => setSelectedCompany(null)} />;
    }
    switch (activeTab) {
      case "landing":
        return <LandingView onNavigate={navigate} onShowAuth={() => setShowAuthModal(true)} user={user} />;
      case "companies":
        return <CompaniesView onSelectCompany={setSelectedCompany} />;
      case "overview":
        return <OverviewView token={token} />;
      case "portfolio":
        return <PortfolioView token={token} onAuthRequired={() => setShowAuthModal(true)} />;
      case "pricing":
        return <PricingView user={user} onShowAuth={() => setShowAuthModal(true)} />;
      case "profile":
        return (
          <ProfileView
            user={user}
            token={token}
            onLogout={handleLogout}
            onNavigate={navigate}
            onShowAuth={() => setShowAuthModal(true)}
          />
        );
      default:
        return <LandingView onNavigate={navigate} onShowAuth={() => setShowAuthModal(true)} user={user} />;
    }
  };

  if (typeof window !== "undefined" && window.location.pathname === "/_design") {
    return <DesignSystem />;
  }

  return (
    <div data-theme={theme} className="app-layout tw-bg-bg-base tw-text-text-primary">
      <Sidebar
        activeTab={selectedCompany ? null : activeTab}
        setActiveTab={navigate}
        theme={theme}
        toggleTheme={() => setTheme(t => t === "dark" ? "light" : "dark")}
        user={user}
      />

      <main className="app-main">
        {renderView()}
      </main>

      {showAuthModal && (
        <AuthModal onClose={() => setShowAuthModal(false)} onSuccess={handleLogin} />
      )}
    </div>
  );
}
